package main

import "fmt"

func main() {
	var day = 1
	switch day {
	case (1):

		fmt.Println("monday")
	case (2):

		fmt.Println("tuesday")
	case (3):
		fmt.Println("wednesday")
	case (4):

		fmt.Println("thursday")
	case (5):
		fmt.Println("friday")
	case (6):
		fmt.Println("saturday")
	case (7):
		fmt.Println("sunday")
	}
}
